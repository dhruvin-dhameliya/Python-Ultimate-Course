# Range Function in Python.
# This function using a sequence of number print.


# Print the number 0 to 7.
for i in range(8):
    print(i)  # Start 0


print("----------")


# Print the number 1 to 7.
for i in range(1, 8):
    print(i) # Start 1


print("----------")


# Print the number 2 to 7.
for i in range(2, 8):
    print(i)   # Start 2

print("----------")

# Syntex:
            # range(Start, Stop, Step_Size )
for i in range(1, 8, 2):
    print(i)         